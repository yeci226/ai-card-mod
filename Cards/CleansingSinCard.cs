/// <summary>
/// 68. 瘣蔽
/// </summary>
[Pool(typeof(ProphetCardPool))]
public class CleansingSinCard : CustomCardModel
{
    private const int energyCost = 1;
    private const CardType type = CardType.Skill;
    private const CardRarity rarity = CardRarity.Common;
    private const TargetType targetType = TargetType.None;
    private const bool shouldShowInLibrary = true;

    public CleansingSinCard() : base(energyCost, type, rarity, targetType, shouldShowInLibrary) { }

    protected override async Task OnPlay(PlayerChoiceContext choiceContext, CardPlay cardPlay)
    {
        int healPerCard = IsUpgraded ? 3 : 2;
        await CreatureCmd.Heal(Owner.Creature, healPerCard * 2);
    }

    protected override void OnUpgrade()
    {
        // Upgrade effects handled in OnPlay if needed
    }
}

